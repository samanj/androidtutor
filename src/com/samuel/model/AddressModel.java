package com.samuel.model;

public class AddressModel {
	private String StudentCountry;
	private String StudentCity;
	private String StudentStreet;
	private String StudentPincode;
	
	public String getStudentCountry() {
		return StudentCountry;
	}
	public void setStudentCountry(String studentCountry) {
		StudentCountry = studentCountry;
	}
	public String getStudentCity() {
		return StudentCity;
	}
	public void setStudentCity(String studentCity) {
		StudentCity = studentCity;
	}
	public String getStudentStreet() {
		return StudentStreet;
	}
	public void setStudentStreet(String studentStreet) {
		StudentStreet = studentStreet;
	}
	public String getStudentPincode() {
		return StudentPincode;
	}
	public void setStudentPincode(String studentPincode) {
		StudentPincode = studentPincode;
	}
	

}
